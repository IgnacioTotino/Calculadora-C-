using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculadora2
{
    class OperadorMultiplicar
    {

        public double EstaMultiplicando(double N1, double N2)
        {
            double MULT = N1 * N2;

            if (N1 == 0 || N2 == 0) return MULT = 0;
            else return MULT;

        }

    }
}
